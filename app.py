# –§–ª–∞–≥ –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è, –±—ã–ª –ª–∏ –±–æ—Ç —É–∂–µ –∑–∞–ø—É—â–µ–Ω
bot_started = False
